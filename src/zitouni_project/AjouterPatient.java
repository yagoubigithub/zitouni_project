    private void setMedecinToJcombo(){
        jComboBox2.removeAllItems();
        
        unite = jComboBox1.getSelectedItem().toString();
        
        ArrayList<Medecin> medecins_par_unite = db.getMedecinParUnite(unite,getNuitOrJour());
        for (int i = 0; i < medecins_par_unite.size(); i++) {
            Medecin medecin = medecins_par_unite.get(i);
            System.out.println(medecin.getPersonne());
            jComboBox2.addItem("Dr " + medecin.getPersonne().getNom() + " "+ medecin.getPersonne().getPrenom());
        }
    }
    private String  getNuitOrJour() {
        Calendar cal = Calendar.getInstance();
        SimpleDateFormat sdf = new SimpleDateFormat("HH");
        int heur = Integer.parseInt(sdf.format(cal.getTime()));
       
        if(heur >= 8 && heur <= 17){
            return "jour";
        }else{
         return "nuit";   
        }
    }
