    private void kButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_kButton1ActionPerformed
       
            
            String nom = nom_input.getText();
            
            if(nom.trim().length() == 0){
                nom_input.setBackground(Color.red);
                return;
            }else{
                nom_input.setBackground(Color.WHITE);
            }
            
            String prenom = prenom_input.getText();
            
            if(prenom.trim().length() == 0){
                prenom_input.setBackground(Color.red);
                return;
            }else{
                prenom_input.setBackground(Color.WHITE);
            }
            
            String adresse = adresse_input.getText();
            String tele = tele_input.getText();
            
            
            try {
                int tele_int = Integer.parseInt(tele);
                tele_input.setBackground(Color.WHITE);
            } catch (NumberFormatException e) {
                tele_input.setBackground(Color.red);
                return;
            }
            
            String date_naissance = datePicker1.getDateStringOrEmptyString();
            
            
            
            String sexe  = jRadioButton1.isSelected() ? jRadioButton1.getText() : jRadioButton2.getText();
            
            
            String nb_seance = nb_se_input.getText();
            try {
                int nb_seance_int = Integer.parseInt(nb_seance);
                nb_se_input.setBackground(Color.WHITE);
            } catch (NumberFormatException e) {
                nb_se_input.setBackground(Color.red);
                return;
            }
            
            String nom_medecin_consult = nom_medecin_consult_input.getText().trim().equals("") ? ""  :nom_medecin_consult_input.getText();
            
            String dg = dg_input.getText().trim().equals("") ? ""  :dg_input.getText();
            String date_dg = datePicker2.getDateStringOrEmptyString().trim().equals("") ? ""  : datePicker2.getDateStringOrEmptyString();
            
            String unite = jComboBox1.getSelectedItem().toString();
         
         
            String nom_medecin = jComboBox2.getSelectedItem().toString().trim().equals("") ? ""  :jComboBox2.getSelectedItem().toString();
            
            int id_medecin = db.getIdMedecinFromNom(nom_medecin);
            int id_unite = db.getIdUniteFromName(unite);
            isSave = db.insertPatient(nom, prenom, date_naissance, adresse, sexe,
                    tele, Integer.parseInt(nb_seance),nom_medecin_consult,dg,date_dg,id_medecin,id_unite);
            
            if(isSave){
            dispose();
            }
        
         
    }//GEN-LAST:event_kButton1ActionPerformed

